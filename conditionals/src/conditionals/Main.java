package conditionals;

public class Main {

	public static void main(String[] args) {
		int number = 26;
		
		//switch
		if(number<20) {
			System.out.println("The number is less to 20");
		}else if(number==20) {
			System.out.println("The number is equal to 20");
		}else {
			System.out.println("The number is greater to 20");
		}
		//defensive programming

	}

}
